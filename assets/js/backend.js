function JugarMontyHall(PuertaSeleccionada){
    var i;
    var AuxPuerta;
    switch (EleccionNumero) {                                //Verificamos si se acciono una jugada
        case 0:                                              //0: Es la primera vez que abrimos una puerta.
            EleccionNumero++;                                //Incrementamos el Contador de NÂ° de elecciones.
            PuertaGanadora = GenerarNumAleatorio(1,3);       //Seleccionamos como ganadora una puerta al azar.
            SeleccionarPuerta(PuertaSeleccionada);           //Marcamos la puerta seleccionada por el usuario.
            if (PuertaSeleccionada!=PuertaGanadora){         //Verificamos si la puerta seleccionada es la ganadora.
                for (i=1;i<=3;i++) {                         //Si la puerta seleccionada no es la ganadora
                    if ((i!==PuertaGanadora) && (i!==PuertaSeleccionada)) { //Abrimos una puerta vacia.
                        CambiarSonidoMH(0);
                        AbrirPuerta(i);
                        DeshabilitarPuerta(i);
                    }
                }
            }   else {                                      //Si la puerta seleccionada es la ganadora.
                switch (PuertaSeleccionada){                //Y la puerta seleccionada es:
                    case 1:                                 //1: Abrimos una puerta al azar (2 o 3) y la deshabilitamos
                        AuxPuerta = (GenerarNumAleatorio(2,3))
                        CambiarSonidoMH(0);
                        AbrirPuerta(AuxPuerta);
                        DeshabilitarPuerta(AuxPuerta);
                    break;
                    case 2:
                        CambiarSonidoMH(0);                 //2: Abrimos la puerta 3 y la deshabilitamos
                        AbrirPuerta(3);
                        DeshabilitarPuerta(3);
                    break;
                    case 3:
                        AuxPuerta = (GenerarNumAleatorio(1,2))//3: Abrimos una puerta al azar (1 o 2) y la deshabilitamos
                        CambiarSonidoMH(0);
                        AbrirPuerta(AuxPuerta);
                        DeshabilitarPuerta(AuxPuerta);
                    break;
                }
            }
            break;
        case 1:
            if (PuertaSeleccionada == PuertaGanadora) {     //Si el usuario gano:
                DeshabilitarTodasLasPuertas();              //Deshabilitamos todas las puertas
                CambiarSonidoMH(0);                         //Reproducimos el sonido de abrir
                AbrirPuertaGanadora(PuertaSeleccionada);    //Abrimos la puerta ganadora
                EleccionNumero = 0;                         //Reseteamos nuestro contador de elecciones
                ContadorDeIntentos++;                       //Incrementamos el numero de intentos
                ContadorDeVictorias++;                      //Incrementamos el contador de victorias
                if (ContadorDeVictorias<3) {                //Verificamos si el usuario gano
                    ActualizarContadorDeLlaves(ContadorDeVictorias);    //Actualizamos las llaves en pantalla
                    ReiniciarJuego();                                   //Reiniciamos el juego
                }   else{                                   //El else significa que el usuario gano
                    EstadoMontyHall.textContent = 'Felicidades!';
                    window.setTimeout(function (){;
                        DeshabilitarTodasLasPuertas();
                        CerrarTodasLasPuertas();
                        EstadoMontyHall.textContent = 'Has afrontado un reto probabilÃ­stico.';
                        window.setTimeout(function (){;
                            CambiarSonidoMH(0);
                            AbrirPuertaGanadora(1);
                            window.setTimeout(function (){;
                                CambiarSonidoMH(0);
                                AbrirPuertaGanadora(2);
                                window.setTimeout(function (){;
                                    CambiarSonidoMH(0);
                                    AbrirPuertaGanadora(3);
                                    EstadoMontyHall.textContent = 'Y has salido victorioso!';
                                    window.setTimeout(function (){;
                                        CerrarTodasLasPuertas();
                                        EstadoMontyHall.textContent = 'Puedes volver, cuando quieras, a jugar.';
                                        window.setTimeout(function (){;
                                            PauseLoop();
                                            CambiarSonidoMH(0);
                                            AbrirTodasLasPuertas();
                                            window.setTimeout(function (){;
                                                OcultarMontyHall();
                                                MostrarReproductor();
                                                BotonPlay.style.display="none";                         //Ocultamos el boton de play.
                                                ContinuarReproduciendoMusica = true;
                                                MatematicasVisitado();
                                                CambiarMusica(NumMusica);
                                                Video2_2_1();
                                            }, 500)
                                        }, 3000);
                                    }, 1000);
                                }, 1000);
                            }, 1000);
                        }, 3000);
                    }, 2000);
                    
                }
            } else {                             //Este else se acciona si el usuario selecciona la puerta errada.
                DeshabilitarTodasLasPuertas();
                CambiarSonidoMH(0);     
                AbrirPuertaGanadora(PuertaGanadora);
                AbrirPuerta(PuertaSeleccionada)
                EleccionNumero = 0;
                ContadorDeIntentos++;
                ReiniciarJuego();
            }
            break;
    }
}

